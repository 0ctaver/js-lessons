//бинарный плюс - относится к двум операндам
let s = "моя" + "строка";
document.write(s + "<br>"); // моястрока

let x = 2 + 2 + '1';
document.write(x + "<br>");

//унарный плюс. если операнд не число, унарный плюс преобразует его в число
// Преобразует нечисла в числа
document.write( +true  + "<br>"); // 1
document.write( +"" + "<br>");   // 0

let apples = "2";
let oranges = "3";

// оба операнда предварительно преобразованы в числа
document.write( +apples + +oranges + "<br>"); // 5

//присваивание. работает справа-налево. Сначала вычисляется самое правое выражение 2 + 2, и затем оно присваивается переменным слева: c, b и a
let a, b, c;
a = b = c = 2 + 2;

//Остаток от деления %
document.write( 5 % 2 + "<br>"); // 1, остаток от деления 5 на 2

//Возведение в степень **
document.write( 2 ** 3  + "<br>"); // 8  (2 * 2 * 2) 2 в 3 степени

//Инкремент/декремент
let counter = 2;
counter++;        // работает как counter = counter + 1, просто запись короче. увеличивает на 1. декремент уменьшает на 1
document.write( counter  + "<br>"); // 3
//Инкремент/декремент можно применить только к переменной! 5++ не работает

