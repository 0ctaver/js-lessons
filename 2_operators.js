//совмещенные операторы. Сокращённая арифметика с присваиванием
let n = 2;
n += 5; // теперь n=7 (работает как n = n + 5)
n *= 2; // теперь n=14 (работает как n = n * 2)

document.write( n + "<br>"); // 14

//Оператор запятая
